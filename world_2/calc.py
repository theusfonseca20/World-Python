# O que é um modulo?

# São arquivos em python que podem conter funções, classes e variáveis.
# Módulos podem ser importados usando a palavra chave import.
# Os módulos também permitem a reutilização do código.
# Simplicidade ("Dividir para conquistar")
# Melhor manutenibilidade no código
# Isolamento no projeto de software
# Facilita colaboração entre desenvolvedores e outros integrantes

def soma(a,b):
    result = a+b
    return result

def subtracao(a,b):
    result = a-b
    return result

def mult(a,b):
    result = a*b
    return result

def div(a,b):
    result = a/b
    return result
